"""
# Bonus 3 (Unpacking)
Given the following tuple, use unpacking to assign the elements to individual variables:
Assign each element to separate variables `a`, `b`, `c`, `d`, and `e`.
# Print the variables
print(a)  # Output: 10
print(b)  # Output: 20
print(c)  # Output: 30
print(d)  # Output: 40
print(e)  # Output: 50    

"""

# Solution
# Given tuple
given_tuple = (10, 20, 30, 40, 50)

# Unpacking the tuple and assigning elements to variables a, b, c, d, and e.
a, b, c, d, e = given_tuple

# Print the variables
print(a)  # Output: 10
print(b)  # Output: 20
print(c)  # Output: 30
print(d)  # Output: 40
print(e)  # Output: 50
